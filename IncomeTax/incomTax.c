#include<stdio.h>


double incomTax(double num){
    double tmp;

    if(num>=80000){
        tmp = (num-80000)*0.45 + (80000-55000)*0.35 + (55000-35000)*0.3 + (35000-9000)*0.25 + (9000-4500)*0.2 + (4500-1500)*0.1 + 1500*0.03;
    }else if( num >= 55000 && num<80000){
        
        tmp = (num-55000)*0.35 + (55000-35000)*0.3 + (35000-9000)*0.25 + (9000-4500)*0.2 + (4500-1500)*0.1 + 1500*0.03;
    }else if( num >= 35000 && num <55000 ){
        tmp = (num-35000)*0.3 + (35000-9000)*0.25 + (9000-4500)*0.2 + (4500-1500)*0.1 + 1500*0.03;
        
    }else if(num >= 9000 && num < 35000){
        tmp = (num-9000)*0.25 + (9000-4500)*0.2 + (4500-1500)*0.1 + 1500*0.03;
        
    }else if(num >= 4500 && num < 9000){
        tmp = (num-4500)*0.2 + (4500-1500)*0.1 + 1500*0.03;

    }else if(num >= 1500 && num < 4500 ){
        tmp = (num-1500)*0.1 + 1500*0.03;
        
    }else{
        tmp = num*0.03;
        
    }


    return (num-tmp);

}

